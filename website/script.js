const navToggle = document.querySelector(".nav-toggle");
const navLinks = document.querySelectorAll(".site-nav a");
const languageButtons = document.querySelectorAll("[data-lang-switch]");
const metaDescription = document.querySelector('meta[name="description"]');

let currentLanguage = getInitialLanguage();

function getInitialLanguage() {
  const requestedLanguage = new URLSearchParams(window.location.search).get("lang");
  if (requestedLanguage === "en" || requestedLanguage === "pt") {
    return requestedLanguage;
  }

  try {
    const savedLanguage = localStorage.getItem("oakhimedes-language");
    return savedLanguage === "en" ? "en" : "pt";
  } catch {
    return "pt";
  }
}

function translate(key) {
  return translations[currentLanguage][key] ?? translations.pt[key] ?? "";
}

function setNavState(isOpen) {
  if (!navToggle) {
    return;
  }

  navToggle.setAttribute("aria-expanded", String(isOpen));
  navToggle.setAttribute("aria-label", translate(isOpen ? "nav.close" : "nav.open"));
  document.body.classList.toggle("nav-open", isOpen);
}

function applyLanguage(language) {
  currentLanguage = language === "en" ? "en" : "pt";
  document.documentElement.lang = currentLanguage === "en" ? "en" : "pt-PT";
  document.title = translate("meta.title");

  if (metaDescription) {
    metaDescription.setAttribute("content", translate("meta.description"));
  }

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const value = translate(element.dataset.i18n);
    if (value) {
      element.innerHTML = value;
    }
  });

  document.querySelectorAll("[data-i18n-attr]").forEach((element) => {
    element.dataset.i18nAttr.split(";").forEach((pair) => {
      const [attribute, key] = pair.split(":");
      const value = translate(key);
      if (attribute && value) {
        element.setAttribute(attribute, value);
      }
    });
  });

  languageButtons.forEach((button) => {
    const isActive = button.dataset.langSwitch === currentLanguage;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });

  setNavState(document.body.classList.contains("nav-open"));

  try {
    localStorage.setItem("oakhimedes-language", currentLanguage);
  } catch {
    // Language persistence is optional.
  }
}

if (navToggle) {
  navToggle.addEventListener("click", () => {
    const isOpen = navToggle.getAttribute("aria-expanded") !== "true";
    setNavState(isOpen);
  });
}

navLinks.forEach((link) => {
  link.addEventListener("click", () => {
    setNavState(false);
  });
});

languageButtons.forEach((button) => {
  button.addEventListener("click", () => {
    applyLanguage(button.dataset.langSwitch);
  });
});

applyLanguage(currentLanguage);
